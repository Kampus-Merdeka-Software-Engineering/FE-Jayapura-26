var hnewsclasslist = ["mainHeading","top-heading","top-heading2","secondary-heading"]
var hnewsclasslisti = 0;

headlinenews.forEach(function (chnews) {
    hnewsclasslisti = 0;
    var hslider = document.getElementById("headingnews-slider");
    var chnewselement = document.createElement("div");
    chnewselement.classList.add("swiper-slide", "slide-headline-container-add");
    var gchnewselement = document.createElement("div");
    gchnewselement.classList.add("grid-news-container");
    chnews.forEach(function (hnews) {
        var hnewselement = document.createElement("a");
        hnewselement.classList.add(hnewsclasslist[hnewsclasslisti]);
        hnewselement.href = hnews.link;
        hnewselement.style.backgroundImage = `url(${hnews.imgurl})`;
        hnewselement.innerHTML = `<h1>${hnews.judul}</h1><h3>${hnews.penors}</h3>`;
        gchnewselement.appendChild(hnewselement);
        hnewsclasslisti = hnewsclasslisti + 1;
    })
    chnewselement.appendChild(gchnewselement);
    hslider.appendChild(chnewselement);
})

//general news
